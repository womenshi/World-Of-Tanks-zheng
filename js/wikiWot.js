$(function(){
	var cText="",lText="",tabTank="",tankInfo=[];
	$("#wiki .type a,#wiki .tabL a").click(function(){
		if(!$(this).hasClass("curr")){
			if($(this).parent().hasClass("type")){
				cText=$(this).attr("data-c");
				lText=$("#wiki .tabL a:eq(0)").attr("data-l");
				$("#wiki .tabL a").removeClass("curr").eq(0).addClass("curr");
			}else{
				lText=$(this).attr("data-l");
			}
			$(this).addClass("curr").siblings().removeClass("curr");	
			tabTank="",tankInfo=[];
			for(var i in wikiWot[cText][lText]){
				var data=wikiWot[cText][lText][i];
				tabTank+="<a href='javascript:;' data-name='"+data.name+"'><img src='images/WOT/t"+data.type+".png'>"+data.name+"</a>";
				tankInfo[data.name]="<div class='tankName'><h4>"+data.name+"</h4><p>"+data.description+"</p></div>"
						   +"<div class='tankDe'><p>"+data.content+"</p><a href='"+data.href+"' target='_blank'>查看详情 &gt;&gt;</a></div>"
						   +"<div class='tankImg'><img src='"+data.src+"'></div>";
			};
			$(".tabTank").html(tabTank).find("a").click(function(){
				if(!$(this).hasClass("curr")){
					var _name=$(this).attr("data-name");
					$(this).addClass("curr").siblings().removeClass("curr");
					$(".tankInfo").html(tankInfo[_name]);	
				}
			}).eq(0).trigger("click");
		}
	}).eq(0).trigger("click");	
})